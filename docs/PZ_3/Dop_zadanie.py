"""
Ввести 2 числа. Если их произведение отрицательно, умножить его на 8, в противном
случае увеличить его в 1.5 раза.

"""

a = float(input("Введите первое число: "))
b = float(input("Введите второе число: "))
product = a * b
if product < 0:
    result = product * 8
    print(f"Произведение отрицательное: {product}")
    print(f"Результат после умножения на 8: {result}")
else:
    result = product * 1.5
    print(f"Произведение неотрицательное: {product}")
    print(f"Результат после умножения на 1.5: {result}")
"""
Вести число. Если оно четное, разделить его на 4, если нечетное - умножить на 5.
"""
num = int(input("Введите число: "))
if num % 2 == 0:
    result = num / 4
    print(f"Число {num} четное")
    print(f"Результат после деления на 4: {result}")
else:
    result = num * 5
    print(f"Число {num} нечетное")
    print(f"Результат после умножения на 5: {result}")

"""
Ввести двухзначное число. Если сумма цифр числа четная, то увеличить число на 2,
в противном случае уменьшить на 2.
"""

num = int(input("Введите двузначное число: "))
if 10 <= num <= 99:
    digit1 = num // 10  # первая цифра
    digit2 = num % 10   # вторая цифра
    digit_sum = digit1 + digit2
    if digit_sum % 2 == 0:
        result = num + 2
        print(f"Сумма цифр ({digit1} + {digit2} = {digit_sum}) четная")
        print(f"Результат после увеличения на 2: {result}")
    else:
        result = num - 2
        print(f"Сумма цифр ({digit1} + {digit2} = {digit_sum}) нечетная")
        print(f"Результат после уменьшения на 2: {result}")
else:
    print("Ошибка! Введите двузначное число (от 10 до 99)")
"""
Дано целое число. Если оно является положительным, то прибавить к нему 20, в
противном случае вычесть из него 5. 
"""
num = int(input("Введите целое число: "))
if num > 0:
    result = num + 20
    print(f"Число {num} положительное")
    print(f"Результат после прибавления 20: {result}")
else:
    result = num - 5
    print(f"Число {num} не положительное")
    print(f"Результат после вычитания 5: {result}")


"""
Дано два числа. Если их сумма кратна 5, то прибавить 1, иначе вычесть 2.
"""

a = float(input("Введите первое число: "))
b = float(input("Введите второе число: "))
sum_ab = a + b
if sum_ab % 5 == 0:
    result = sum_ab + 1
    print(f"Сумма чисел ({a} + {b} = {sum_ab}) кратна 5")
    print(f"Результат после прибавления 1: {result}")
else:
    result = sum_ab - 2
    print(f"Сумма чисел ({a} + {b} = {sum_ab}) не кратна 5")
    print(f"Результат после вычитания 2: {result}")


